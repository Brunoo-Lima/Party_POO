package match_aula.es2;

public class Question {
    private String text;
    private String answers;
    public String getText() {
        return text;
    }
    public void setText(String text) {
        this.text = text;
    }
    public String getAnswers() {
        return answers;
    }
    public void setAnswers(String answers) {
        this.answers = answers;
    }
}
